

// Assignment operations

import static java.lang.System.*;

public class Assignment
{
   public static void main( String args[] )
   {
  int num = 10;
  out.println(num);  //10

  num = num + 5;
  out.println(num);  //15

  num = 10 * 2 + 7;
  out.println(num);  //27

  num *= 2;
  out.println(num);  //54

  num /= 5;
  out.println(num);  //10

  num = num + 4 / 2 - 8;
  out.println(num);  //4

  num = (4 + 5)/2+7;
  out.println(num);  //11
   }
}

